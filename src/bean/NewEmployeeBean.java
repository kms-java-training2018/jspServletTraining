package bean;

public class NewEmployeeBean {
    // フィールドの宣言
    private String name;
    private int empNum;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getEmpNum() {
        return empNum;
    }

    public void setEmpNum(int empNum) {
        this.empNum = empNum;
    }
}
